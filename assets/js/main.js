document.addEventListener("DOMContentLoaded", function () {
  let currentIndex = 0;
  const techBoxes = document.querySelectorAll(".tech_box");
  const nextButton = document.querySelector(".next");
  const prevButton = document.querySelector(".prev");
  const totalBoxes = techBoxes.length;

  function showBox(index) {
    techBoxes.forEach((box, i) => {
      if (i === index) {
        box.classList.add("active");
      } else {
        box.classList.remove("active");
      }
    });
  }

  function nextBox() {
    currentIndex = (currentIndex + 1) % totalBoxes;
    showBox(currentIndex);
  }

  function prevBox() {
    currentIndex = (currentIndex - 1 + totalBoxes) % totalBoxes;
    showBox(currentIndex);
  }

  let autoSlide = setInterval(nextBox, 2000);

  nextButton.addEventListener("click", function () {
    nextBox();
    clearInterval(autoSlide);
    autoSlide = setInterval(nextBox, 2000); 
  });

  prevButton.addEventListener("click", function () {
    prevBox();
    clearInterval(autoSlide);
    autoSlide = setInterval(nextBox, 2000);
  });

  showBox(currentIndex);
});


